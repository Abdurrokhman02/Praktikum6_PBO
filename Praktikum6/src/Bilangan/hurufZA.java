public class hurufZA {
    public void ZkeA() {
        System.out.println("C. Huruf Z - A");
        System.out.print("Huruf dari Z - A: ");
        for (char huruf = 'Z'; huruf >= 'A'; huruf--) {
            System.out.print(huruf + " ");
        }
        System.out.println();
        System.out.println("===================================================");
    }
}
